/**
 * Pulls the OpenMoji icons this app ships into src/assets/openmoji, and writes the manifest
 * that indexes them.
 *
 * Run by hand when the icon set needs adding to or refreshing:
 *   node scripts/fetch-openmoji.mjs
 *
 * The icons are committed, so a normal install and build never touch the network. OpenMoji is
 * not a dependency either: the full package is ~43MB for the ~140 icons used here, so this
 * fetches just those from the CDN at a pinned version rather than making everyone install the
 * rest. The SVGs are written byte-for-byte as published — they're CC BY-SA 4.0, and leaving
 * them unmodified keeps the redistribution terms simple (attribution, no adaptation).
 */
import { mkdir, writeFile, readdir, rm } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const OPENMOJI_VERSION = '17.0.0'
const CDN = `https://unpkg.com/openmoji@${OPENMOJI_VERSION}`

/** Unicode files clock faces and weather under travel-places too; neither is a place, and a
 * point-of-interest picker full of clock faces helps nobody. */
const EXCLUDED_SUBGROUPS = new Set(['time', 'sky-weather'])
const GROUP = 'travel-places'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const outDir = join(root, 'src/assets/openmoji')
const manifestPath = join(root, 'src/openmojiManifest.ts')

/** Modest concurrency: enough to finish quickly, gentle enough not to get rate-limited. */
async function mapLimit(items, limit, fn) {
  const results = []
  let cursor = 0
  await Promise.all(
    Array.from({ length: Math.min(limit, items.length) }, async () => {
      while (cursor < items.length) {
        const index = cursor++
        results[index] = await fn(items[index])
      }
    }),
  )
  return results
}

async function fetchText(url) {
  const response = await fetch(url)
  if (!response.ok) throw new Error(`${response.status} ${response.statusText} — ${url}`)
  return response.text()
}

console.log(`OpenMoji ${OPENMOJI_VERSION}: reading catalogue…`)
const catalogue = JSON.parse(await fetchText(`${CDN}/data/openmoji.json`))

const wanted = catalogue
  .filter(entry => entry.group === GROUP && !EXCLUDED_SUBGROUPS.has(entry.subgroups))
  // Skin-tone and other variants share an annotation with their base and would only clutter
  // the picker; the base emoji is the one worth offering.
  .filter(entry => !entry.skintone)
  .map(entry => ({
    hexcode: entry.hexcode,
    emoji: entry.emoji,
    name: entry.annotation,
    subgroup: entry.subgroups,
  }))
  .sort((a, b) => a.subgroup.localeCompare(b.subgroup) || a.name.localeCompare(b.name))

console.log(`  ${wanted.length} icons in ${GROUP} (minus ${[...EXCLUDED_SUBGROUPS].join(', ')})`)

await rm(outDir, { recursive: true, force: true })
await mkdir(outDir, { recursive: true })

let bytes = 0
await mapLimit(wanted, 8, async icon => {
  const svg = await fetchText(`${CDN}/color/svg/${icon.hexcode}.svg`)
  bytes += Buffer.byteLength(svg)
  await writeFile(join(outDir, `${icon.hexcode}.svg`), svg)
})

const written = (await readdir(outDir)).length
console.log(`  wrote ${written} svg files, ${(bytes / 1024).toFixed(0)} kB total`)

const manifest = `// Generated by scripts/fetch-openmoji.mjs — do not edit by hand.
// OpenMoji ${OPENMOJI_VERSION}, group "${GROUP}" (excluding ${[...EXCLUDED_SUBGROUPS].join(', ')}).
// Icons are CC BY-SA 4.0; see the Legal dialog for the attribution this ships with.

export interface OpenMojiIcon {
  /** Unicode codepoint(s), and the SVG's filename in src/assets/openmoji. */
  hexcode: string
  /** The character itself, handy for search and for a text fallback. */
  emoji: string
  /** Human-readable name, as OpenMoji annotates it. */
  name: string
  /** OpenMoji's own subgrouping — the natural way to section a picker. */
  subgroup: string
}

export const OPENMOJI_ICONS: OpenMojiIcon[] = ${JSON.stringify(wanted, null, 2)}
`

await writeFile(manifestPath, manifest)
console.log(`  wrote ${manifestPath.replace(root + '/', '')}`)
